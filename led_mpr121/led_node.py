class Node(object):
    def __init__(self):
        self.spi = spidev.SpiDev()
        self.spi.open(0,0)
        self.spi.mode = 0b11
        self.spi.max_speed_hz = 1000000
        self.spi.bits_per_word = 8
        self.spi.lsbfirst=False
        rospy.Subscriber("led", String, self.callback)


    def buildIcon(self, template, color):
        icon=start_frame[:]
        for i in range(15,7,-1): #i=ligne
            for j in range(0,8): #j= colonne
                if template[i][j] == 0:
                    icon += LED_OFF
                else:
                    icon += color
        
        for i in range(7,-1,-1):
            for j in range(0,8):
                if template[i][j] == 0:
                    icon += LED_OFF
                else:
                    icon += color

        for i in range(15,7,-1):
            for j in range(8,16):
                if template[i][j] == 0:
                    icon += LED_OFF
                else:
                    icon += color

        for i in range(7,-1,-1):
            for j in range(8,16):
                if template[i][j] == 0:
                    icon += LED_OFF
                else:
                    icon += color

        icon += end_frame
        return icon

